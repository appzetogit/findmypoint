export function loadAdminUsers(): UserProfile[] {
  const data = localStorage.getItem("fmp_admin_users:v1");
  if (!data) {
    localStorage.setItem("fmp_admin_users:v1", JSON.stringify(INITIAL_USERS));
    return INITIAL_USERS;
  }
  try {
    return JSON.parse(data);
  } catch (e) {
    console.error("Failed to parse admin users from localStorage", e);
    return INITIAL_USERS;
  }
}

export function saveAdminUsers(users: UserProfile[]): void {
  localStorage.setItem("fmp_admin_users:v1", JSON.stringify(users));
}
